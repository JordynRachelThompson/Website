function donutChartTotal1() {
    var totalSpent = parseInt(document.getElementById('totalSpent1').innerHTML);
    var totalBudget = parseInt(document.getElementById('totalBudget1').innerHTML);

    var budgetData = [['Total Spent', 'Remaining Budget'], ['Total Spent', totalSpent], ['Remaining Budget', totalBudget]];

    google.charts.load("current", { packages: ["corechart"] });
    google.charts.setOnLoadCallback(drawChart);
    function drawChart() {
        var data = google.visualization.arrayToDataTable(budgetData);
        var options = {
            titleTextStyle: {
                color: '#cfd8dc',
                bold: true,
                fontName: 'arial'
            }
            ,
            colors: ['#e6c5c4', '#6cb7b1'],
            pieHole: 0.5,
            backgroundColor: 'black',
            color: '#cfd8dc',
            is3D: true,
            width: 700,
            height: 600,
            fontSize: 25,
            bold: true
        };
        var chart = new google.visualization.PieChart(document.getElementById('donutchartTotal1'));
        chart.draw(data, options);
    }
};

